#include "miniRT.h"

size_t	skip_space(char *str)
{
	size_t	i;

	i = 0;
	while (str[i] == ' ' || str[i] == '\t')
		i++;
	return (i);
}

void	inclement_ij(size_t *i, size_t *j)
{
	(*i)++;
	(*j)++;
}

