#include "miniRT.h"

void	free_txt(char **txt)
{
	size_t	i;

	i = 0;
	while (txt[i])
	{
		free(txt[i]);
		txt[i] = NULL;
		i++;
	}
	free(txt);
	txt = NULL;
}
